  <kw-action-top>
    <!-- 삭제 -->
    <kw-btn
      v-show="!props.readonly"
      :label="$t('MSG_BTN_DEL')"
      grid-action
      dense
      :disable="gridRowCount === 0"
      @click="onClickRemove"
    />
  </kw-action-top>
  <ul class="filter-box justify-between mb12">
    <li class="filter-box__item">
      <p class="filter-box__item-label">
        <!-- 판매채널 -->
        {{ $t('MSG_TXT_SEL_CHNL') }}
      </p>
      <kw-select
        v-model="filterChannel"
        dense
        first-option="all"
